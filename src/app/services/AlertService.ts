import { Injectable } from '@angular/core';
import { AlertInterface, AlertOptions } from '../models/AlertInterface';
import Swal from 'sweetalert2';

@Injectable({
  providedIn: 'root',
})
export class AlertService implements AlertInterface {
  success(options: AlertOptions): Promise<void> {
    return Swal.fire({
      title: options.title,
      text: options.text,
      icon: 'success',
      timer: options.duration ? options.duration : undefined,
      showConfirmButton: options.dismissible !== false,
    }).then(() => {});
  }

  error(options: AlertOptions): Promise<void> {
    return Swal.fire({
      title: options.title,
      text: options.text,
      icon: 'error',
      timer: options.duration ? options.duration : undefined,
      showConfirmButton: options.dismissible !== false,
    }).then(() => {});
  }

  warning(options: AlertOptions): Promise<void> {
    return Swal.fire({
      title: options.title,
      text: options.text,
      icon: 'warning',
      timer: options.duration ? options.duration : undefined,
      showConfirmButton: options.dismissible !== false,
    }).then(() => {});
  }

  info(options: AlertOptions): Promise<void> {
    return Swal.fire({
      title: options.title,
      text: options.text,
      icon: 'info',
      timer: options.duration ? options.duration : undefined,
      showConfirmButton: options.dismissible !== false,
    }).then(() => {});
  }
}
